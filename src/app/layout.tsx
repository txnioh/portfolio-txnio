import type { Metadata } from "next";
import localFont from "next/font/local";
import "./globals.css";
import { Inter } from 'next/font/google';
import PageTransition from "./components/PageTransition";

const geistSans = localFont({
  src: "./fonts/GeistVF.woff",
  variable: "--font-geist-sans",
  weight: "100 900",
});
const geistMono = localFont({
  src: "./fonts/GeistMonoVF.woff",
  variable: "--font-geist-mono",
  weight: "100 900",
});

const inter = Inter({ subsets: ['latin'] });

export const metadata: Metadata = {
  title: "Antonio Gonzalez",
  description: "Generated by txnio",
  icons: {
    icon: [
      { url: '/o_30px_white.png', type: 'image/png' },
    ],
  },
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" data-theme="dark">
      <head>
        <link rel="preload" href="/fonts/ChicagoKare-Regular.woff" as="font" type="font/woff" crossOrigin="anonymous" />
        <link rel="preload" href="/fonts/Renogare-Regular.otf" as="font" type="font/otf" crossOrigin="anonymous" />
        <link rel="preload" href="/fonts/geneva-12.otf" as="font" type="font/otf" crossOrigin="anonymous" />
        <link rel="preload" href="/fonts/ark-pixel-zh_tw.woff2" as="font" type="font/woff2" crossOrigin="anonymous" />
        <style>{`
          body, html {
            margin: 0;
            padding: 0;
            height: 100%;
            width: 100%;
            overflow: visible;
            background-color: var(--portfolio-bg);
            color: var(--portfolio-text);
            transition: background-color 0.3s ease, color 0.3s ease;
          }
          #__next {
            height: 100%;
            width: 100%;
          }
          #content-wrapper {
            opacity: 1;
            transition: opacity 0.3s ease-in;
          }
        `}</style>
        <script dangerouslySetInnerHTML={{
          __html: `
            (function() {
              try {
                var savedTheme = window.localStorage.getItem('portfolio-theme');
                if (savedTheme === 'light' || savedTheme === 'dark') {
                  document.documentElement.dataset.theme = savedTheme;
                }
              } catch (e) {}

              window.addEventListener('load', function() {
                var wrapper = document.getElementById('content-wrapper');
                if (wrapper) {
                  wrapper.style.opacity = '1';
                }
              });
            })();
          `
        }} />
      </head>
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased ${inter.className}`}
      >
        <div id="content-wrapper">
          <PageTransition>{children}</PageTransition>
        </div>
      </body>
    </html>
  );
}
